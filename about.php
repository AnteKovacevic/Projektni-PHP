		<p>Sara Blakely had her product--an undergarment that smoothed the contours of a woman's body, making clothes more flattering and comfortable. But she had yet to make her first sale. She managed to land a meeting with a buyer at a Neiman Marcus store in Dallas. Clad in a pair of formfitting white pants, Blakely invited the buyer to join her in the ladies' room, where she proceeded to demonstrate the difference in the way the pants looked--with her undergarments, which she had dubbed Spanx, and without. Three weeks later, Spanx was on the shelves. She pulled the same stunt with buyers for Bloomingdale's, Saks, and Bergdorf Goodman. "I wore those white pants for three years to sell Spanx," Blakely says.</p>
		<p>Before it has investors, customers, profits, press coverage, or even a perfected product, every startup has at least one valuable asset: its story. So you might want to ask yourself: Who are you? Where did you come from? Why are you doing this? </p>
		<p>Even if you're answering these questions only for yourself, your co-founders, or the guy sitting next to you on the plane (hey, he asked), your company's origin story has more power than you might imagine. And that's true whether it begins in a garage (à la Hewlett-Packard, Apple, and Google), a dorm room (Dell), the streets of a far-off country (Toms Shoes), or the offices of a former employer (most everyone else). </p>
		
	
